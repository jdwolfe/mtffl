<h1>Rule Amendment History</h1>

<p><b><a name="amendment1" id="amendment1"></a><u>Amendment 1.</u> 2000</b>: Increased number of Freeze players from 5 to 6 players</p>

<p><b><a name="amendment2" id="amendment2"></a><u>Amendment 2.</u> 2001</b>: Increased number of Freeze players from 6 to 7 players</p>

<p><b><a name="amendment3" id="amendment3"></a><u>Amendment 3.</u> 2001</b>: Added a bonus point structure to offensive players.<br>
Players get an extra 3 points for passing more than 300 yards in one week.<br>
Players get an extra 3 points for rushing or receiving for more than 100 yards in one week.</p>

<p><strong><a name="amendment4" id="amendment4"></a></strong><b><u>Amendment 4.</u></b> <strong>2003</strong>: Changed Draft Order assignment procedure. Now the 8 teams that don't make the playoffs will have their own tournament to decide Draft Order for the next season..</p>

<p><strong><a name="amendment5" id="amendment5"></a></strong><b><u>Amendment 5.</u></b> <strong>2004</strong>: Post season ties are broken by the total starter points in the regular season.</p>

<p><strong><a name="amendment6" id="amendment6"></a></strong><b><u>Amendment 6.</u></b> <strong>2004</strong>: Home Field Advantage (HFA) reduced to 7 points (from 10 points) for all regular season games and 1st round post season games.</p>

<p><strong><a name="amendment7" id="amendment7"></a></strong><b><u>Amendment 7.</u></b> <strong>2004</strong>: All 2nd round post season games, including the championship game, are exempt from the Home Field Advantage (HFA) rule. Games will be played on a 'neutral' field.</p>

<p><strong><a name="amendment8" id="amendment8"></a></strong><b><u>Amendment 8.</u></b> <strong>2005</strong>: Trades are prohibited from the start of Week 11 games until the end of the MTFFL championship game.</p>

<p><strong><a name="amendment9" id="amendment9"></a></strong><b><u>Amendment 9.</u></b> <strong>2006</strong>: Both leagues switch to MyFantasyLeague.com</p>

<p><strong><a name="amendment10" id="amendment10"></a></strong><b><u>Amendment 10.</u></b> <strong>2006</strong>: Ammendment 4 is overturned and draft position is determined by regular season results.</p>

<p><strong><a name="amendment11" id="amendment11"></a></strong><b><u>Amendment 11.</u></b> <strong>2015</strong>: Home Field Advantage (HFA) is abolished.</p>

